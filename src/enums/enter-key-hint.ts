/**
 * Hints what action label (or icon) to use to represent the enter key on
 * virtual keyboards.
 *
 * @enum {string}
 */
enum EnterKeyHint {
  done = 'done',
  enter = 'enter',
  go = 'go',
  next = 'next',
  previous = 'previous',
  search = 'search',
  send = 'send'
}

export default EnterKeyHint
